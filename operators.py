x = 10
y = 5

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x ** y)
print(x % y)

x = 10
x += 5  # Increment x by 5
print(x)  # Output: 15

x -= 3  # Decrement x by 3
print(x)  # Output: 12

x *= 2  # Multiply x by 2
print(x)  # Output: 24

x /= 4  # Divide x by 4
print(x)  # Output: 6.0

